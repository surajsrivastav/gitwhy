func TestDashes(t *testing.T) {
	result := dashes(5)
	if result != "─────" {
		t.Errorf("expected 5 dashes, got %q", result)
	}

	result = dashes(0)
	if result != "" {
		t.Errorf("expected empty string for 0 dashes, got %q", result)
	}

	result = dashes(50)
	if utf8.RuneCountInString(result) != 50 {
		t.Errorf("expected 50 dashes, got %d runes", utf8.RuneCountInString(result))
	}

	result = dashes(49)
	if utf8.RuneCountInString(result) != 49 {
		t.Errorf("expected 49 dashes, got %d runes", utf8.RuneCountInString(result))
	}
}

