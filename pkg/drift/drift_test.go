func TestEstimateAffectedLines(t *testing.T) {
	tmpDir := t.TempDir()
	specPath := filepath.Join(tmpDir, "spec.yaml")
	content := "line1\nline2\nline3\nline4\nline5\n"
	os.WriteFile(specPath, []byte(content), 0644)

	report, err := DetectDrift("f.go", "S-1", specPath, "oldhash", "human")
	if err != nil {
		t.Fatal(err)
	}

	if report.AffectedLines <= 0 {
		t.Logf("affected lines: %d (may vary based on hash comparison)", report.AffectedLines)
	}
}
