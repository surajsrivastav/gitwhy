func TestHashSpec(t *testing.T) {
	tmpDir := t.TempDir()
	specPath := filepath.Join(tmpDir, "spec.yaml")
	content := "key: value\nnested:\n  field: test\n"

	if err := os.WriteFile(specPath, []byte(content), 0644); err != nil {
		t.Fatal(err)
	}

	hash, err := HashSpec(specPath)
	if err != nil {
		t.Fatalf("HashSpec() error = %v", err)
	}

	if len(hash) != 16 {
		t.Errorf("expected 16 char hash, got %d: %s", len(hash), hash)
	}

	hash2, _ := HashSpec(specPath)
	if hash != hash2 {
		t.Error("hash should be deterministic")
	}
}

func TestHashSpecNonExistent(t *testing.T) {
	_, err := HashSpec("/nonexistent/path")
	if err == nil {
		t.Error("expected error for non-existent spec file")
	}
}

func TestHashSpecContent(t *testing.T) {
	h1 := HashSpecContent("hello")
	h2 := HashSpecContent("hello")
	h3 := HashSpecContent("world")

	if h1 != h2 {
		t.Error("hash should be deterministic")
	}
	if h1 == h3 {
		t.Error("different content should produce different hashes")
	}
}

func TestDetectDrift(t *testing.T) {
	tmpDir := t.TempDir()
	specPath := filepath.Join(tmpDir, "spec.yaml")
	initialContent := "version: 1\nendpoints:\n  - /users\n  - /posts\n"

	if err := os.WriteFile(specPath, []byte(initialContent), 0644); err != nil {
		t.Fatal(err)
	}

	specHash, _ := HashSpec(specPath)

	report, err := DetectDrift("src/api/handlers.go", "BLUE-204", specPath, specHash, "agent:blue")
	if err != nil {
		t.Fatalf("DetectDrift() error = %v", err)
	}

	if report.DriftDetected {
		t.Error("expected no drift when spec hasn't changed")
	}
	if report.Severity != SeverityNone {
		t.Errorf("expected severity none, got %v", report.Severity)
	}
	if report.Spec != "BLUE-204" {
		t.Errorf("expected spec BLUE-204, got %q", report.Spec)
	}
	if report.GeneratedBy != "agent:blue" {
		t.Errorf("expected generated by agent:blue, got %q", report.GeneratedBy)
	}
}

func TestDetectDriftWithChange(t *testing.T) {
	tmpDir := t.TempDir()
	specPath := filepath.Join(tmpDir, "spec.yaml")
	if err := os.WriteFile(specPath, []byte("original content"), 0644); err != nil {
		t.Fatal(err)
	}

	specHash, _ := HashSpec(specPath)

	if err := os.WriteFile(specPath, []byte("modified content with new endpoints and features"), 0644); err != nil {
		t.Fatal(err)
	}

	report, err := DetectDrift("src/api/handlers.go", "BLUE-204", specPath, specHash, "agent:blue")
	if err != nil {
		t.Fatalf("DetectDrift() error = %v", err)
	}

	if !report.DriftDetected {
		t.Error("expected drift detected when spec changed")
	}
	if report.Severity != SeverityHigh {
		t.Errorf("expected severity high, got %v", report.Severity)
	}
	if report.CurrentSpecHash == "" {
		t.Error("expected current spec hash to be set")
	}
	if report.SpecHashAtGen != specHash {
		t.Errorf("expected spec hash at gen %q, got %q", specHash, report.SpecHashAtGen)
	}
}

