func TestReportString(t *testing.T) {
	r := &Report{
		File:            "src/auth/tenant.ts",
		Spec:            "BLUE-204",
		GeneratedAt:     "2026-03-14T00:00:00Z",
		GeneratedBy:     "agent:blue",
		SpecHashAtGen:   "abc123f",
		CurrentSpecHash: "def456a",
		DriftDetected:   true,
		Severity:        SeverityHigh,
		AffectedLines:   47,
		Recommendation:  "DRIFT DETECTED — spec has changed since code generation",
	}

	s := r.String()
	if s == "" {
		t.Error("expected non-empty report string")
	}
	if !strings.HasPrefix(s, "  gitwhy drift report\n  ") {
		t.Error("expected report to start with header followed by separator")
	}

	if c := strings.Count(s, "DRIFT DETECTED — spec has changed since code generation"); c < 2 {
		t.Errorf("expected at least 2 occurrences of drift message, got %d", c)
	}
	if !strings.Contains(s, "DRIFT DETECTED") {
		t.Error("expected drift detection message")
	}

	lines := strings.Split(s, "\n")
	if len(lines) > 1 && utf8.RuneCountInString(strings.TrimSpace(lines[1])) != 50 {
		t.Error("expected exactly 50 dashes in separator line")
	}

	if !strings.Contains(s, "─────") {
		t.Error("expected dashes separator")
	}
	if !strings.Contains(s, "file:") {
		t.Error("expected file field")
	}
	if !strings.Contains(s, "spec:") {
		t.Error("expected spec field")
	}
	if !strings.Contains(s, "generated:") {
		t.Error("expected generated field")
	}
	if !strings.Contains(s, "2026-03-14") {
		t.Error("expected generation date in output")
	}
	if strings.Contains(s, "2026-03-14T") {
		t.Error("expected date without T separator in output")
	}
	if !strings.Contains(s, "2026-03-14  by agent:blue") {
		t.Error("expected date without T separator next to 'by' field")
	}
	if !strings.Contains(s, "spec hash:") {
		t.Error("expected spec hash field")
	}
	if !strings.Contains(s, "abc123f") {
		t.Error("expected SpecHashAtGen hash in output")
	}
	if !strings.Contains(s, "def456a") {
		t.Error("expected CurrentSpecHash hash in output")
	}
	if !strings.Contains(s, "\n\n  DRIFT DETECTED") {
		t.Error("expected blank line before drift section")
	}
	if !strings.Contains(s, "Lines affected:") {
		t.Error("expected Lines affected field")
	}
	if !strings.Contains(s, "+47") {
		t.Error("expected affected line count in output")
	}
}

